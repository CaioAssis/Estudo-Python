# input: pede input para o usuario. Normalmente grava como string
# type(): retorna o tipo da variavel
# Ex:
# <class 'str'>
# <class 'int'>
# <class 'float'>
nome = input("Digite seu nome: ")
idade = int (input("Digite sua idade: "))
